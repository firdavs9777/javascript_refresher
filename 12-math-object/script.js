// Round a number to the nearest integer
x = Math.round(4.6);
console.log('Math.round(4.6):', x); // 5

// Round a number down
x = Math.floor(4.9);
console.log('Math.floor(4.9):', x); // 4

// Round a number up
x = Math.ceil(4.1);
console.log('Math.ceil(4.1):', x); // 5

// Absolute value
x = Math.abs(-50);
console.log('Math.abs(-50):', x); // 50

// Power (exponentiation)
x = Math.pow(2, 3);
console.log('Math.pow(2, 3):', x); // 8

// Square root
x = Math.sqrt(25);
console.log('Math.sqrt(25):', x); // 5

// Minimum and Maximum of a list of numbers
x = Math.min(1, 2, 3);
console.log('Math.min(1, 2, 3):', x); // 1
x = Math.max(1, 2, 3);
console.log('Math.max(1, 2, 3):', x); // 3

// Random number between 0 and 1
x = Math.random();
console.log('Math.random():', x); // e.g., 0.123456789